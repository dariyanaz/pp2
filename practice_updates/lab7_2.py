# –ò–º–ø–æ—Ä—Ç –Ω–µ–æ–±—Ö–æ–¥–∏–º—ã—Ö –±–∏–±–ª–∏–æ—Ç–µ–∫
import pygame  # –û—Å–Ω–æ–≤–Ω–∞—è –±–∏–±–ª–∏–æ—Ç–µ–∫–∞ –¥–ª—è –º—É–ª—å—Ç–∏–º–µ–¥–∏–∞

# –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è –≤—Å–µ—Ö –º–æ–¥—É–ª–µ–π Pygame
pygame.init()

# –°–æ–∑–¥–∞–Ω–∏–µ –≥—Ä–∞—Ñ–∏—á–µ—Å–∫–æ–≥–æ –æ–∫–Ω–∞ 
screen = pygame.display.set_mode((400, 300))
# –£—Å—Ç–∞–Ω–æ–≤–∫–∞ –∑–∞–≥–æ–ª–æ–≤–∫–∞ –æ–∫–Ω–∞
pygame.display.set_caption("Music Player")

# –§—É–Ω–∫—Ü–∏—è –¥–ª—è –æ—Ç–æ–±—Ä–∞–∂–µ–Ω–∏—è –≥–æ—Ä—è—á–∏—Ö –∫–ª–∞–≤–∏—à –≤ –∫–æ–Ω—Å–æ–ª–∏
def show_hotkeys():
    print("üéµ –ì–æ—Ä—è—á–∏–µ –∫–ª–∞–≤–∏—à–∏ –º—É–∑—ã–∫–∞–ª—å–Ω–æ–≥–æ –ø–ª–µ–µ—Ä–∞:")
    print("üîπ SPACE - –í–æ—Å–ø—Ä–æ–∏–∑–≤–µ—Å—Ç–∏/–ü—Ä–æ–¥–æ–ª–∂–∏—Ç—å")
    print("üîπ P - –ü–∞—É–∑–∞")
    print("üîπ S - –û—Å—Ç–∞–Ω–æ–≤–∏—Ç—å –º—É–∑—ã–∫—É")
    print("üîπ N - –°–ª–µ–¥—É—é—â–∏–π —Ç—Ä–µ–∫")
    print("üîπ B - –ü—Ä–µ–¥—ã–¥—É—â–∏–π —Ç—Ä–µ–∫")

# –í—ã–≤–æ–¥–∏–º –ø–æ–¥—Å–∫–∞–∑–∫–∏ –ø–æ —É–ø—Ä–∞–≤–ª–µ–Ω–∏—é –ø—Ä–∏ –∑–∞–ø—É—Å–∫–µ
show_hotkeys()

# –°–ø–∏—Å–æ–∫ –º—É–∑—ã–∫–∞–ª—å–Ω—ã—Ö —Ñ–∞–π–ª–æ–≤ –¥–ª—è –≤–æ—Å–ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∏—è
musics = ['–ù—é—à–∞ –∏ –ö–∞–π—Ä–∞—Ç –ù—É—Ä—Ç–∞—Å - –ê–ª–º–∞—Ç—ã —Ç—É–Ω–¥–µ—Ä–∏.mp3','Naughty Boy Ft. Sam Smith - LaLaLa.mp3','Ulukmanapo - –õ–µ—Ç–∞–ª–∏.mp3','V S X V Prince - –¶–≤–µ—Ç—ã.mp3','D-Block Europe ft. Central Cee - Overseas.mp3']
# –ò–Ω–¥–µ–∫—Å —Ç–µ–∫—É—â–µ–≥–æ —Ç—Ä–µ–∫–∞ (–Ω–∞—á–∏–Ω–∞–µ–º —Å –ø–µ—Ä–≤–æ–≥–æ)
track = 0
# –§–ª–∞–≥ —Å–æ—Å—Ç–æ—è–Ω–∏—è –ø–∞—É–∑—ã (False - –Ω–µ –Ω–∞ –ø–∞—É–∑–µ, True - –Ω–∞ –ø–∞—É–∑–µ)
paused = False

# –ó–∞–≥—Ä—É–∂–∞–µ–º –ø–µ—Ä–≤—ã–π —Ç—Ä–µ–∫ –¥–ª—è –≤–æ—Å–ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∏—è
pygame.mixer.music.load(musics[track])

# –§—É–Ω–∫—Ü–∏—è –≤–æ—Å–ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∏—è/–ø—Ä–æ–¥–æ–ª–∂–µ–Ω–∏—è –º—É–∑—ã–∫–∏
def play():
    global paused  # –ò—Å–ø–æ–ª—å–∑—É–µ–º –≥–ª–æ–±–∞–ª—å–Ω—É—é –ø–µ—Ä–µ–º–µ–Ω–Ω—É—é paused
    if paused:
        # –ï—Å–ª–∏ –º—É–∑—ã–∫–∞ –±—ã–ª–∞ –Ω–∞ –ø–∞—É–∑–µ, —Å–Ω–∏–º–∞–µ–º –ø–∞—É–∑—É
        pause()  # –í—ã–∑—ã–≤–∞–µ–º pause() –¥–ª—è –ø–µ—Ä–µ–∫–ª—é—á–µ–Ω–∏—è —Å–æ—Å—Ç–æ—è–Ω–∏—è
    else:
        # –ï—Å–ª–∏ –Ω–µ –Ω–∞ –ø–∞—É–∑–µ, –Ω–∞—á–∏–Ω–∞–µ–º –≤–æ—Å–ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∏–µ
        pygame.mixer.music.play()
        print("‚ñ∂ –ù–∞—á–∞–ª–æ –≤–æ—Å–ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∏—è")

# –§—É–Ω–∫—Ü–∏—è –æ—Å—Ç–∞–Ω–æ–≤–∫–∏ –º—É–∑—ã–∫–∏
def stop():
    pygame.mixer.music.stop()
    print("‚èπ –í–æ—Å–ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∏–µ –æ—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–æ")

# –§—É–Ω–∫—Ü–∏—è –ø–∞—É–∑—ã/–ø—Ä–æ–¥–æ–ª–∂–µ–Ω–∏—è
def pause():
    global paused
    if paused:
        # –ï—Å–ª–∏ –Ω–∞ –ø–∞—É–∑–µ, –ø—Ä–æ–¥–æ–ª–∂–∞–µ–º –≤–æ—Å–ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∏–µ
        pygame.mixer.music.unpause()
        paused = False
        print("‚ñ∂ –í–æ—Å–ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∏–µ –ø—Ä–æ–¥–æ–ª–∂–µ–Ω–æ")
    else:
        # –ï—Å–ª–∏ –Ω–µ –Ω–∞ –ø–∞—É–∑–µ, —Å—Ç–∞–≤–∏–º –Ω–∞ –ø–∞—É–∑—É
        pygame.mixer.music.pause()
        paused = True
        print("‚è∏ –ü–∞—É–∑–∞")

# –§—É–Ω–∫—Ü–∏—è –ø–µ—Ä–µ–∫–ª—é—á–µ–Ω–∏—è –Ω–∞ —Å–ª–µ–¥—É—é—â–∏–π —Ç—Ä–µ–∫
def next():
    global track, paused  # –ò—Å–ø–æ–ª—å–∑—É–µ–º –≥–ª–æ–±–∞–ª—å–Ω—ã–µ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ
    paused = False  # –°–Ω–∏–º–∞–µ–º –ø–∞—É–∑—É –ø—Ä–∏ –ø–µ—Ä–µ–∫–ª—é—á–µ–Ω–∏–∏ —Ç—Ä–µ–∫–∞
    # –£–≤–µ–ª–∏—á–∏–≤–∞–µ–º –∏–Ω–¥–µ–∫—Å —Ç—Ä–µ–∫–∞ —Å –∑–∞—Ü–∏–∫–ª–∏–≤–∞–Ω–∏–µ–º (% len(musics))
    track = (track + 1) % len(musics)
    # –ó–∞–≥—Ä—É–∂–∞–µ–º –Ω–æ–≤—ã–π —Ç—Ä–µ–∫
    pygame.mixer.music.load(musics[track])
    # –ù–∞—á–∏–Ω–∞–µ–º –≤–æ—Å–ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∏–µ
    play()
    print(f"üéµ –°–µ–π—á–∞—Å –∏–≥—Ä–∞–µ—Ç: {musics[track]}")

# –§—É–Ω–∫—Ü–∏—è –ø–µ—Ä–µ–∫–ª—é—á–µ–Ω–∏—è –Ω–∞ –ø—Ä–µ–¥—ã–¥—É—â–∏–π —Ç—Ä–µ–∫
def prev():
    global track, paused
    paused = False
    # –£–º–µ–Ω—å—à–∞–µ–º –∏–Ω–¥–µ–∫—Å —Ç—Ä–µ–∫–∞ —Å –∑–∞—Ü–∏–∫–ª–∏–≤–∞–Ω–∏–µ–º
    track = (track - 1) % len(musics)
    pygame.mixer.music.load(musics[track])
    play()
    print(f"üéµ –°–µ–π—á–∞—Å –∏–≥—Ä–∞–µ—Ç: {musics[track]}")

# –û—Å–Ω–æ–≤–Ω–æ–π —Ü–∏–∫–ª –ø—Ä–æ–≥—Ä–∞–º–º—ã
running = True
while running:
    # –ó–∞–ª–∏–≤–∞–µ–º —ç–∫—Ä–∞–Ω —á–µ—Ä–Ω—ã–º —Ü–≤–µ—Ç–æ–º (RGB: 0,0,0))
    screen.fill((0, 0, 0))
    
    # –û–±—Ä–∞–±–æ—Ç–∫–∞ —Å–æ–±—ã—Ç–∏–π Pygame
    for event in pygame.event.get():
        if event.type == pygame.QUIT:
            # –ï—Å–ª–∏ –∑–∞–∫—Ä—ã–ª–∏ –æ–∫–Ω–æ, –≤—ã—Ö–æ–¥–∏–º –∏–∑ —Ü–∏–∫–ª–∞
            running = False
        elif event.type == pygame.KEYDOWN:
            # –û–±—Ä–∞–±–æ—Ç–∫–∞ –Ω–∞–∂–∞—Ç–∏–π –∫–ª–∞–≤–∏—à
            if event.key == pygame.K_SPACE:
                play()  # –ü—Ä–æ–±–µ–ª - –≤–æ—Å–ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∏–µ/–ø—Ä–æ–¥–æ–ª–∂–µ–Ω–∏–µ
            elif event.key == pygame.K_p:
                pause()  # P - –ø–∞—É–∑–∞/–ø—Ä–æ–¥–æ–ª–∂–µ–Ω–∏–µ
            elif event.key == pygame.K_s:
                stop()   # S - –æ—Å—Ç–∞–Ω–æ–≤–∫–∞
            elif event.key == pygame.K_n:
                next()   # N - —Å–ª–µ–¥—É—é—â–∏–π —Ç—Ä–µ–∫
            elif event.key == pygame.K_b:
                prev()  # B - –ø—Ä–µ–¥—ã–¥—É—â–∏–π —Ç—Ä–µ–∫
    
    # –û—Ç–æ–±—Ä–∞–∂–µ–Ω–∏–µ –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏–∏ –æ —Ç–µ–∫—É—â–µ–º —Ç—Ä–µ–∫–µ –∏ —Å—Ç–∞—Ç—É—Å–µ
    font = pygame.font.SysFont('Arial', 20)  # –®—Ä–∏—Ñ—Ç –¥–ª—è —Ç–µ–∫—Å—Ç–∞
    # –û–ø—Ä–µ–¥–µ–ª—è–µ–º —Ç–µ–∫—É—â–∏–π —Å—Ç–∞—Ç—É—Å
    if paused:
        status = "–ü–∞—É–∑–∞"
    elif pygame.mixer.music.get_busy():
        status = "–í–æ—Å–ø—Ä–æ–∏–∑–≤–µ–¥–µ–Ω–∏–µ"
    else:
        status = "–û—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–æ"
    # –°–æ–∑–¥–∞–µ–º —Ç–µ–∫—Å—Ç–æ–≤—É—é –ø–æ–≤–µ—Ä—Ö–Ω–æ—Å—Ç—å
    text = font.render(f"{musics[track]} - {status}", True, (255, 255, 255))
    # –û—Ç–æ–±—Ä–∞–∂–∞–µ–º —Ç–µ–∫—Å—Ç –≤ –æ–∫–Ω–µ (–∫–æ–æ—Ä–¥–∏–Ω–∞—Ç—ã 20,20)
    screen.blit(text, (20, 20))
    
    # –û–±–Ω–æ–≤–ª—è–µ–º —ç–∫—Ä–∞–Ω
    pygame.display.flip()

# –ó–∞–≤–µ—Ä—à–µ–Ω–∏–µ —Ä–∞–±–æ—Ç—ã Pygame –ø–æ—Å–ª–µ –≤—ã—Ö–æ–¥–∞ –∏–∑ —Ü–∏–∫–ª–∞
pygame.quit()